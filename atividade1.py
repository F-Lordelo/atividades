# Atividade 1 - Concatenando Dados
# Descrição: Receber dois dados diferentes do usuário e concatená-los em uma única string

# Solicitando entrada de dados do usuário
dado1 = input("Digite o primeiro dado: ")
dado2 = input("Digite o segundo dado: ")

# Concatenando os dados em uma única string
resultado = dado1 + " " + dado2

# Exibindo o resultado
print(f"\nResultado da concatenação: {resultado}")